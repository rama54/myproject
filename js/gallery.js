"use strict";
$(document).ready(function() {
    /*----------------------------------------------------
     PORTFOLIO JS
     ----------------------------------------------------*/

    var $container = $('.galleryContainer');
    $container.isotope({
        filter: '*',
        animationOptions: {
            duration: 750,
            easing: 'linear',
            queue: false
        }
    });
    $('.galleryFilter li .btn_tab').on('click', function() {
        $('.galleryFilter .current').removeClass('current');
        $(this).addClass('current');

        var selector = $(this).attr('data-filter');
        $container.isotope({
            filter: selector,
            animationOptions: {
                duration: 750,
                easing: 'linear',
                queue: false
            }
        });
        return false;
    });
});
